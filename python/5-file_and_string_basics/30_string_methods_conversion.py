# 30_string_methods_and_conversion.py
# 문자열 메서드, 유효성 검사, 공백 제거, 입력값 변환 예제 모음

# ------------------------------------------------------------
# 1) 문자열 내 특정 문자 포함 여부 ('in' 연산자)
# ------------------------------------------------------------
msg = input('임의의 문장을 입력하세요: ')
if 'a' in msg:
    print('당신이 입력한 문장에는 a가 있습니다.')
else:
    print('당신이 입력한 문장에는 a가 없습니다.')

# ------------------------------------------------------------
# 2) 문자열 내 특정 단어 포함 여부 ('is')
# ------------------------------------------------------------
msg = input('\n임의의 문장을 입력하세요: ')
if 'is' in msg:
    print('당신이 입력한 문장에는 is가 있습니다.')
else:
    print('당신이 입력한 문장에는 is가 없습니다.')

# ------------------------------------------------------------
# 3) 문자열 길이 계산
# ------------------------------------------------------------
msg = input('\n임의의 문장을 입력하세요: ')
msglen = len(msg)
# msglen = len(msg.encode())  # 한글 포함 시 바이트 길이 확인 가능
print('당신이 입력한 문장의 길이는 <%d> 입니다.' % msglen)

# ------------------------------------------------------------
# 4) 문자열이 모두 문자(isalpha)
# ------------------------------------------------------------
txt1 = 'A'
txt2 = '안녕'
txt3 = 'Warcraft Three'
txt4 = '3PO'
print('\n[isalpha] 문자 판별 테스트:')
print(txt1, txt1.isalpha())  # True
print(txt2, txt2.isalpha())  # True
print(txt3, txt3.isalpha())  # False
print(txt4, txt4.isalpha())  # False

# ------------------------------------------------------------
# 5) 문자열이 모두 숫자인지(isdigit)
# ------------------------------------------------------------
txt1 = '010-1234-5678'
txt2 = 'R2D2'
txt3 = '1212'
print('\n[isdigit] 숫자 판별 테스트:')
print(txt1, txt1.isdigit())  # False
print(txt2, txt2.isdigit())  # False
print(txt3, txt3.isdigit())  # True

# ------------------------------------------------------------
# 6) 문자열이 영숫자인지(isalnum)
# ------------------------------------------------------------
txt1 = '안녕하세요?'
txt2 = '1. Title-제목을 넣으세요'
txt3 = '3피오R2D2'
ret1 = txt1.isalnum()
ret2 = txt2.isalnum()
ret3 = txt3.isalnum()
print('\n[isalnum] 영문자/숫자 판별:')
print(txt1, ret1)  # False
print(txt2, ret2)  # False
print(txt3, ret3)  # True

# ------------------------------------------------------------
# 7) 문자열 대소문자 변환 (upper/lower)
# ------------------------------------------------------------
txt = 'A lot of Things occur each day.'
ret1 = txt.upper()
ret2 = txt.lower()
print('\n[대소문자 변환]')
print(ret1)  # ‘A LOT OF THINGS OCCUR EACH DAY.’
print(ret2)  # ‘a lot of things occur each day.’

# ------------------------------------------------------------
# 8) 문자열 공백 제거 (strip, lstrip, rstrip)
# ------------------------------------------------------------
txt = '   양쪽에 공백이 있는 문자열입니다.   '
ret1 = txt.lstrip()
ret2 = txt.rstrip()
ret3 = txt.strip()
print('\n[공백 제거 테스트]')
print('<' + txt + '>')
print('<' + ret1 + '>')
print('<' + ret2 + '>')
print('<' + ret3 + '>')

# ------------------------------------------------------------
# 9) 입력값 숫자형 변환 (int, float)
# ------------------------------------------------------------
numstr = input('\n숫자를 입력하세요: ')
try:
    num = int(numstr)
    print('당신이 입력한 숫자는 정수 <%d>입니다.' % num)
except:
    try:
        num = float(numstr)
        print('당신이 입력한 숫자는 실수 <%f>입니다.' % num)
    except:
        print('+++ 숫자를 입력하세요~ +++')

# ------------------------------------------------------------
# 10) 숫자를 문자열로 변환 (str)
# ------------------------------------------------------------
num1 = 1234
num2 = 3.14
numstr1 = str(num1)
numstr2 = str(num2)
print('\n[숫자 → 문자열 변환]')
print('num1을 문자열로 변환한 값은 “%s” 입니다.' % numstr1)
print('num2를 문자열로 변환한 값은 “%s” 입니다.' % numstr2)
