# 35_dictionary_basics.py
# 딕셔너리 생성, 수정, 삭제, 초기화, 조회, 합계 예제

# ------------------------------------------------------------
# 1) 리스트 2개를 이용해 딕셔너리 생성 (enumerate 활용)
# ------------------------------------------------------------
solar1 = ['태양', '수성', '금성', '지구', '화성', '목성', '토성', '천왕성', '해왕성']
solar2 = ['Sun', 'Mercury', 'Venus', 'Earth', 'Mars', 'Jupiter', 'Saturn', 'Uranus', 'Neptune']

solardict = {}
for i, k in enumerate(solar1):
    val = solar2[i]
    solardict[k] = val

print('[딕셔너리 생성 결과]')
print(solardict)

# ------------------------------------------------------------
# 2) 값 수정 (기존 키 업데이트)
# ------------------------------------------------------------
names = {
    'Mary': 10999, 'Sams': 2111, 'Aimy': 9778, 'Tom': 20245,
    'Michale': 27115, 'Bob': 5887, 'Kelly': 7855
}
names['Aimy'] = 10000  # 값 변경
print('\n[수정 후 딕셔너리]')
print(names)

# ------------------------------------------------------------
# 3) 요소 삭제 (del)
# ------------------------------------------------------------
del names['Sams']
print('\n[삭제 후 딕셔너리]')
print(names)

# ------------------------------------------------------------
# 4) 전체 삭제 (clear)
# ------------------------------------------------------------
names.clear()
print('\n[clear 후 딕셔너리]')
print(names)  # {}

# ------------------------------------------------------------
# 5) keys() - 키 목록 조회
# ------------------------------------------------------------
names = {
    'Mary': 10999, 'Sams': 2111, 'Aimy': 9778, 'Tom': 20245,
    'Michale': 27115, 'Bob': 5887, 'Kelly': 7855
}
ks = names.keys()
print('\n[keys()] 결과:', ks)

for k in ks:
    print('Key:%s \tValue:%d' % (k, names[k]))

# ------------------------------------------------------------
# 6) values() - 값 목록 조회 및 합계 계산
# ------------------------------------------------------------
vals = names.values()
print('\n[values()] 결과:', vals)

vals_list = list(vals)
ret = sum(vals_list)
print('출생아수 총계: %d' % ret)

# ------------------------------------------------------------
# 7) items() - 키와 값 쌍 목록 조회
# ------------------------------------------------------------
items = names.items()
print('\n[items()] 결과:', items)

for item in items:
    print(item)

# ------------------------------------------------------------
# 8) in 연산자 - 특정 키 존재 여부 확인
# ------------------------------------------------------------
k = input('\n이름을 입력하세요: ')
if k in names:
    print('이름이 <%s>인 출생아수는 <%d>명 입니다.' % (k, names[k]))
else:
    print('자료에 <%s>인 이름이 존재하지 않습니다.' % k)
